class TestPBCDistances:
    def test_minimum_image_vs_mdtraj(self):
        if not PDB_FULL.exists():
            pytest.skip("Full system PDB not found")

        ref_path = REF_DIR / "pbc_distances.json"
        if not ref_path.exists():
            pytest.skip("PBC reference not found")

        ref = json.loads(ref_path.read_text())
        ref_d_pbc = np.asarray(ref["d_pbc"])
        pairs = ref["pairs"]
        box_nm = ref["box_nm"]
        box_ang = [b * 10.0 for b in box_nm]

        struct = pyztraj.load_pdb(str(PDB_FULL))
        box = np.diag(box_ang).astype(np.float32)

        ztraj_d = []
        for pair in pairs:
            a, b = int(pair[0]), int(pair[1])
            d = pyztraj.minimum_image_distance(struct.coords[a], struct.coords[b], box)
            ztraj_d.append(d)

        np.testing.assert_allclose(ztraj_d, ref_d_pbc, atol=0.1)


