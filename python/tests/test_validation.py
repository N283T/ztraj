"""Validation tests: compare pyztraj against mdtraj reference values.

Reference JSON files are pre-generated by validation/generate_reference.py
using mdtraj on the 3tvj_I system (MD ATLAS).
"""

import json
from pathlib import Path

import numpy as np
import pytest

import pyztraj

VALIDATION_DIR = Path(__file__).parent.parent.parent / "validation"
DATA_DIR = VALIDATION_DIR / "test_data"
REF_DIR = VALIDATION_DIR / "reference"

PDB = DATA_DIR / "3tvj_I.pdb"
XTC = DATA_DIR / "3tvj_I_R1.xtc"
PDB_FULL = DATA_DIR / "3tvj_I_full.pdb"


def load_ref(name: str) -> dict:
    with open(REF_DIR / f"{name}.json") as f:
        return json.load(f)


@pytest.fixture()
def structure():
    return pyztraj.load_pdb(PDB)


@pytest.fixture()
def frames(structure):
    result = []
    with pyztraj.open_xtc(XTC, structure.n_atoms) as reader:
        for frame in reader:
            result.append(frame.coords.copy())
    return result


class TestRMSD:
    def test_vs_mdtraj(self, structure, frames):
        ref = load_ref("rmsd_backbone")
        ref_vals = np.asarray(ref["values"])
        # Select backbone atoms (N, CA, C, O)
        backbone = np.array(
            [i for i, name in enumerate(structure.atom_names) if name in ("N", "CA", "C", "O")],
            dtype=np.uint32,
        )
        ztraj_vals = np.array(
            [pyztraj.compute_rmsd(f, frames[0], atom_indices=backbone) for f in frames]
        )
        np.testing.assert_allclose(ztraj_vals, ref_vals, atol=0.3)

    def test_atlas_cross_validation(self, structure, frames):
        atlas_path = DATA_DIR / "3tvj_I_RMSD.tsv"
        if not atlas_path.exists():
            pytest.skip("ATLAS RMSD TSV not found")
        atlas = np.loadtxt(atlas_path, skiprows=1, usecols=1)
        backbone = np.array(
            [i for i, name in enumerate(structure.atom_names) if name in ("N", "CA", "C", "O")],
            dtype=np.uint32,
        )
        ztraj_vals = np.array(
            [pyztraj.compute_rmsd(f, frames[0], atom_indices=backbone) for f in frames]
        )
        min_len = min(len(ztraj_vals), len(atlas))
        np.testing.assert_allclose(ztraj_vals[:min_len], atlas[:min_len], atol=0.2)


